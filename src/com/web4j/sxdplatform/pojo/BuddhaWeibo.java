package com.web4j.sxdplatform.pojo;

import java.util.Date;

public class BuddhaWeibo {

	private Long wbId;
	
	private String wbName;
	
	private String wbDesc;
	
	private String wbUrl;
	
	private String createUser;
	
	private Date createTime;

	public Long getWbId() {
		return wbId;
	}

	public void setWbId(Long wbId) {
		this.wbId = wbId;
	}

	public String getWbName() {
		return wbName;
	}

	public void setWbName(String wbName) {
		this.wbName = wbName;
	}

	public String getWbDesc() {
		return wbDesc;
	}

	public void setWbDesc(String wbDesc) {
		this.wbDesc = wbDesc;
	}

	public String getWbUrl() {
		return wbUrl;
	}

	public void setWbUrl(String wbUrl) {
		this.wbUrl = wbUrl;
	}

	public String getCreateUser() {
		return createUser;
	}

	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	
	
}
